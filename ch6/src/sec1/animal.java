package sec1;

public class animal {
	private String name;
	private String type;
	private int legs;
	private int wings;
	private int speed;
	private boolean spine;
	
		
		animal(){
			this("동물","종류",4,0,0,false);}
		animal(String name){
			this(name,"종류",4,0,0,false);}
		animal(String name,String type){
			this(name,type,4,0,0,false);}
		animal(String name,String type,int legs){
			this(name,type,legs,0,0,false);}
		animal(String name,String type,int legs,int wings){
			this(name,type,legs,wings,0,false);}
		animal(String name,String type,int legs,int wings, int speed){
			this(name,type,legs,wings,speed,false);}
		animal(String name,String type,int legs,int wings, int speed,boolean spine){
			this.name=name;
			this.type=type;
			this.legs=legs;
			this.wings=wings;
			this.speed=speed;
			this.spine=spine;}
			
		public void running() {
				System.out.println("동물이 달립니다.");
			}
		public void running(String name) {
			System.out.println(name+"이/가 달립니다.");
		}
		public void running(String name, int speed) {
			System.out.println(name+"이/가 "+speed+"의 속도로 달립니다.");
		}
			
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public int getLegs() {
			return legs;
		}
		public void setLegs(int legs) {
			this.legs = legs;
		}
		public int getWings() {
			return wings;
		}
		public void setWings(int wings) {
			this.wings = wings;
		}
		public int getSpeed() {
			return speed;
		}
		public void setSpeed(int speed) {
			this.speed = speed;
		}
		public boolean isSpine() {
			return spine;
		}
		public void setSpine(boolean spine) {
			this.spine = spine;
		}
		}
		

	
	

